class Solution{
public:	
		
	string removeDups(string S) 
	{
	    // Your code goes here
	    vector<int>alpha(256,0);
	    string ans{};
	    for(char c:S)
	    {
	       alpha[c]++;
	       if(alpha[c]<=1)
	       {
	           ans+=(c);
	       }
	    }
	    return ans;
	}
};

